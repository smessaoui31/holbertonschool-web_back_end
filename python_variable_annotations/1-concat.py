#!/usr/bin/env python3
"""
Module that takes two strings as arguments
and returns a concatenated string

"""


def concat(str1: str, str2: str) -> str:
    """
    Concat two strings and return them concatenated

    Args:

        1 (string) : First string.
        2 (string) : Second string.

    Returns :
        string : The two strings concatenated.
    """
    return str1 + str2
